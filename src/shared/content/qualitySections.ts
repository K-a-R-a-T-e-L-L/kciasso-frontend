export function buildQualitySectionHref(parts: string[]) {
  return `/kachestvo-obrazovaniya/${parts.join("/")}`;
}

export function getQualitySectionRoot(slug: string) {
  return qualitySectionRoots.find((item) => item.slug === slug);
}

export function resolveQualitySectionPath(parts: string[]): QualityResolvedPath | null {
  const [rootSlug, ...subslugs] = parts;
  const root = getQualitySectionRoot(rootSlug);

  if (!root) return null;

  let current: QualitySectionNode = root;
  const parents: QualitySectionNode[] = [];
  const consumed = [root.slug];

  for (const subslug of subslugs) {
    const next = current.children?.find((item) => item.slug === subslug);

    if (!next) return null;

    parents.push(current);
    current = next;
    consumed.push(next.slug);
  }

  return {
    root,
    current,
    parents,
    href: buildQualitySectionHref(consumed),
  };
}

export function getQualityTopTabs(): CardItem[] {
  return qualitySectionRoots.map((item) => ({
    title: item.title,
    href: buildQualitySectionHref([item.slug]),
    description: item.description,
  }));
}

export function getQualitySiblingTabs(resolved: QualityResolvedPath): CardItem[] {
  const parent = resolved.parents[resolved.parents.length - 1];
  const source = parent?.children ?? resolved.current.children ?? [];
  const pathPrefix =
    resolved.parents.length > 0
      ? [resolved.root.slug, ...resolved.parents.slice(1).map((item) => item.slug)]
      : [resolved.root.slug];

  return source.map((item) => ({
    title: item.title,
    href: buildQualitySectionHref([...pathPrefix, item.slug]),
    description: item.description,
  }));
}
